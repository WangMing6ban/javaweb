package com.ming.beans;

public class Admin {
	private int id;
	private String admin_username;
	private String admin_passwd;
	private String admin_realname;
	private String admin_email;
	private String admin_imgurl;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAdmin_username() {
		return admin_username;
	}
	public void setAdmin_username(String admin_username) {
		this.admin_username = admin_username;
	}
	public String getAdmin_passwd() {
		return admin_passwd;
	}
	public void setAdmin_passwd(String admin_passwd) {
		this.admin_passwd = admin_passwd;
	}
	public String getAdmin_realname() {
		return admin_realname;
	}
	public void setAdmin_realname(String admin_realname) {
		this.admin_realname = admin_realname;
	}
	public String getAdmin_email() {
		return admin_email;
	}
	public void setAdmin_email(String admin_email) {
		this.admin_email = admin_email;
	}
	public String getAdmin_imgurl() {
		return admin_imgurl;
	}
	public void setAdmin_imgurl(String admin_imgurl) {
		this.admin_imgurl = admin_imgurl;
	}
	
}
